import { TypeOrmModuleOptions } from '@nestjs/typeorm';
import { ConfigService } from '@nestjs/config';

export const getDatabaseConfig = (configService: ConfigService): TypeOrmModuleOptions => ({
  type: 'postgres', // üîß CAMBIO: De 'mysql' a 'postgres'
  host: configService.get('DB_HOST'),
  port: +configService.get('DB_PORT'),
  username: configService.get('DB_USERNAME'),
  password: configService.get('DB_PASSWORD'),
  database: configService.get('DB_DATABASE'),
  
  // üîß CONFIGURACIONES ESPEC√çFICAS PARA POSTGRESQL
  ssl: configService.get('NODE_ENV') === 'production' ? {
    rejectUnauthorized: false // Para Supabase
  } : false,
  
  entities: [__dirname + '/../**/*.entity{.ts,.js}'],
  migrations: [__dirname + '/../database/migrations/*{.ts,.js}'],
  
  // üîß AQU√ç PUEDES CAMBIAR EL SYNCHRONIZE
  synchronize: false, // Solo en desarrollo

  // üîß SI QUIERES FORZAR SYNCHRONIZE EN PRODUCCI√ìN (NO RECOMENDADO):
  // synchronize: true, // ‚ö†Ô∏è PELIGROSO EN PRODUCCI√ìN
  
  // üîß SI QUIERES DESACTIVAR SYNCHRONIZE EN DESARROLLO:
  // synchronize: false, // Para usar solo migraciones
  
  logging: configService.get('NODE_ENV') === 'development',
  
  // üîß CONFIGURACIONES ADICIONALES PARA POSTGRESQL
  extra: {
    connectionLimit: 10,
    acquireConnectionTimeout: 60000,
    timeout: 60000,
    keepAlive: true,
  },
});